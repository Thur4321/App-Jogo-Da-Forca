    public void perdeu() {
        if (jogo.perdeu()) {
            JOptionPane.showMessageDialog(this, "Você perdeu", "Derrota", 0);
            setEstado(2);
        }
    }

    public int getEstado() {
        return estado;
    }

    public void setEstado(int estado) {
        java.awt.Component[] componentes = jPanel1.getComponents();
        this.estado = estado;
        switch (estado) {
            case CRIACAO:
                for (int i = 0; i < componentes.length; i++) {
                    if (componentes[i] instanceof javax.swing.JTextField) {
                        javax.swing.JTextField texto = (javax.swing.JTextField) componentes[i];
                        texto.setText(null);
                    }
                    jMIDica.setEnabled(false);
                    jTFDica.setEnabled(false);
                    jBLetra.setEnabled(false);
                    jBPalavra.setEnabled(false);
                }
                break;
            case INICIALIZACAO:
                jMIDica.setEnabled(true);
                jTFDica.setText(null);
                jTFDica.setEnabled(true);
                jBLetra.setEnabled(true);
                jBPalavra.setEnabled(true);
                break;
            case FIMDEJOGO:
                for (int i = 0; i < componentes.length; i++) {
                    if (componentes[i] instanceof javax.swing.JTextField) {
                        javax.swing.JTextField texto = (javax.swing.JTextField) componentes[i];
                        texto.setText(null);
                    }
                    jBLetra.setEnabled(false);
                    jBPalavra.setEnabled(false);
                }
                break;

        }
    }

