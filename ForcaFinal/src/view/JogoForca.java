    private void jBPalavraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPalavraActionPerformed
        String palavra = JOptionPane.showInputDialog(this, "Qual a palavra?", "Palavra", 0);
        jTFUltimoPalpite.setText(palavra);
        if (jogo.palavraFinal(palavra)) {
            JOptionPane.showMessageDialog(this, "Você acertou a palavra", "Vencedor", 0);
            setEstado(2);
        } else {
            JOptionPane.showMessageDialog(this, "Não é a palavra", "Tentativa", 0);
            jTFTentativas.setText(String.valueOf(jogo.getTentativas()));
            jLabel7.setIcon(new ImageIcon(getClass().getResource(jogo.trocaImagem())));
        }
        perdeu();
    }//GEN-LAST:event_jBPalavraActionPerformed

