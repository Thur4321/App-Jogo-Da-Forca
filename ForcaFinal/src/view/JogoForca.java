    private void jBLetraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBLetraActionPerformed
       
        String letras = JOptionPane.showInputDialog(this, "Qual a letra?", "Letra", 0);
        
        if (letras.length() > 1){
            JOptionPane.showMessageDialog(this, "Digite uma letra", "Aviso", 0);
            return;
        }
        
        if (!letras.matches("[^\\d]+")){
            JOptionPane.showMessageDialog(this, "Digite uma letra", "Aviso", 0);
            return;
        }
      
        if (jogo.adiciona(letras.charAt(0)) == 1) {
            jogo.adiciona(letras.charAt(0));
            jTFLetraUsada.setText(jogo.letrasUsadas());
            if (!jogo.acertos(letras.charAt(0))) {
                jogo.setTentativas();
                jTFTentativas.setText(String.valueOf(jogo.getTentativas()));
                jLabel7.setIcon(new ImageIcon(getClass().getResource(jogo.trocaImagem())));
            } else {
                jTFPalavra.setText(jogo.letrasAcertadas());
            }
        } else if (jogo.adiciona(letras.charAt(0)) == 0) {
            JOptionPane.showMessageDialog(this, "Letra já utilizada", "Aviso", 0);
        }
        perdeu();
    }//GEN-LAST:event_jBLetraActionPerformed

    private void jMINovoJogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMINovoJogoActionPerformed
        jogo = new Jogo();
        jogo.setLetrasCertas();
        jTFPalavra.setText(jogo.letrasAcertadas());
        jTFTentativas.setText(String.valueOf(jogo.getTentativas()));
        jTFLetraUsada.setText(null);
        jTFUltimoPalpite.setText(null);
        jLabel7.setIcon(new ImageIcon(getClass().getResource(jogo.trocaImagem())));
        setEstado(1);

    }//GEN-LAST:event_jMINovoJogoActionPerformed

